import Foundation

class FilterRouter {

    var appRouter: AppRouterProtocol!
}

// MARK: - FilterRouterInput

extension FilterRouter: FilterRouterInput {
    func close() {
        appRouter.backTransition()
    }

    func openStart() {
        appRouter.dropAll(isError: true)
    }
}
